/**
 * Get the label for a bone.
 * @param {number} index  - The bone index.
 * @returns {string} The bone label.
 */
const getBoneName = (index) => {
	if (index in BONE_NAMES)
		return BONE_NAMES[index];
	else
		return 'Bone' + index;
}

